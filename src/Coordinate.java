
/** Custom built coordinate (2D point) class*/
public class Coordinate {
	
	private float x;
	private float y;
	
	public Coordinate (float x, float y) {
		this.x = x;
		this.y = y;
	}
	
	public float getX() {
		float x = this.x;
		return x;
	}
	
	public float getY() {
		float y = this.y;
		return y;
	}
	
	public void setX(float x) {
		this.x = x;
	}
	
	public void setY(float y) {
		this.y = y;
	}
	
}
